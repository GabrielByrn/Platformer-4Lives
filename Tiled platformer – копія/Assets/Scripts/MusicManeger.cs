// Клас менеджеру музики
// Виконує регулювання музики та звукових ефектів на всіх рівнях
// Запам'ятовує налаштування навіть при виході з гри
using UnityEngine;

public class MusicManeger : MonoBehaviour
{
    private static readonly string MusicPref = "MusicPref";
    private static readonly string SoundEffectsPref = "SoundEffectsPref";
    private float musicFloat, soundEffectsFloat;

    public AudioSource musicAudio;
    public AudioSource[] soundEffectsAudio;

    // При загрузці рівня зчитуються збережені налаштування
    private void Awake()
    {
        levelSoundSettings();
    }

    //Зчитування збережених налаштувань
    private void levelSoundSettings()
    {
        musicFloat = PlayerPrefs.GetFloat(MusicPref);
        soundEffectsFloat = PlayerPrefs.GetFloat(SoundEffectsPref);

        // Встановлення рівня гучності
        musicAudio.volume = musicFloat;

        for(int i = 0; i < soundEffectsAudio.Length; i++)
        {
            soundEffectsAudio[i].volume = soundEffectsFloat;
        }
    }
}
